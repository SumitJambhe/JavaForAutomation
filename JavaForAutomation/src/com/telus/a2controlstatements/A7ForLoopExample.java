package com.telus.a2controlstatements;

public class A7ForLoopExample {
	
	public static void main(String[] args) {
		
		// for(intialization: condition; post increment/decrement)
		//Program to print 1 to 10
		for(int num=1; num<=10; num++)
		{
			System.out.println(num);
		}
		
		System.out.println("***********************************");
		
		for(int num=1; num<=10;)
		{
			System.out.println(num);
			num++;
		}
		
		//Assignment-program to print from 10 to 1
		
		//Nested for loop- we will  see this later
	}

}
