package com.telus.a2controlstatements;

public class A2IfElseStatement {
	
	public static void main(String[] args) {
		
		//2.if else statement
		int age=16;
		
		if (age>=18)
		{
			//true statement will be executed
			System.out.println(age + " is a major age");//22 is a major age

		}
		else
		{
			//false condition will be executed
			System.out.println(age+ " is a minor age");//16 is a minor age
		}
	}

}