package com.telus.a2controlstatements;

public class A5SwitchStatement {
	
	public static void main(String[] args) {
		
		int month=5;
	
		switch (month) {
		
		case 1: System.out.println("Jan Month");
		break;
		
		case 2: System.out.println("Feb Month");
		break;

		case 3: System.out.println("Mar Month");
		break;
		
		case 4: System.out.println("Apr Month");
		break;
		
		case 5: System.out.println("May Month");
		break;
		
		case 6: System.out.println("Jun Month");
		break;
		
		case 7: System.out.println("Jul Month");
		break;
		
		case 8: System.out.println("Aug Month");
		break;
		
		case 9: System.out.println("Sep Month");
		break;
		
		case 10: System.out.println("Oct Month");
		break;
		
		case 11: System.out.println("Nov Month");
		break;
		
		case 12: System.out.println("Dec Month");
		break;
		
		default : System.out.println("Enter the valid month number ");//No need of break

		}
		
	}

}
