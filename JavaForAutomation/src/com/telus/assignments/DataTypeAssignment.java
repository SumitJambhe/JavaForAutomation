public class DataTypeAssignment {

	public static void main(String[] args) {

		// byte data type
		byte age = 26;
		System.out.println("Byte data type value is " + age);

		// short data type
		short mimAccountBalance = 10000;
		System.out.println("Short data type value is "+ mimAccountBalance);

		// integer date type
		int numberOfApplicationForm = 123422245;
		System.out.println("Integer data type value is "+ numberOfApplicationForm);

		// long data type - 'l' needs to be added to the number
		long population = 1232343546576733333l;
		System.out.println("Long data type value is "+ population);

		// float data type - type case float 
		float areaOfCircle = (float) 54.252548946266548666;
		System.out.println("Float data type value is "+ areaOfCircle);

		// float data type - 'f' needs to be added to the number
		float areaOfCircle2 = 54.251448946266548666f;
		System.out.println("Float data type value is "+ areaOfCircle2);

		// double data type 
		double sciReserchValue = 7.2585848946266546f;
		System.out.println("Double data type value is "+ sciReserchValue);

		// boolean data type 
		boolean condition = false;
		System.out.println("Boolean data type value is "+ condition);

		// character data type
		char digit = '1';
		System.out.println("Character data type value is "+ digit);

		// String data type - S in capitals
		String lastName = "Jambhe";
		System.out.println("String data type value is "+ lastName);

	}

}
