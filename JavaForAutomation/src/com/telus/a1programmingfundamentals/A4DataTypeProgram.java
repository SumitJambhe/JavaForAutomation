package com.telus.a1programmingfundamentals;

public class A4DataTypeProgram {
	
	public static void main(String[] args) {
		
		/*
		 * Integer-Range of Number
		 */
		
		//all the java reserved keywords will be in lower case
		//byte data type
		byte month=7;
		//System.out.println("Month Number is 12");
		System.out.println(month);
		System.out.println("Month Number is "+ month);
		
		//short data types
		short quantity=1000;
		System.out.println("The quantity = "+quantity);
		
		
		//integer  data type
		int empID= 12345;
		System.out.println(empID+ "is the employee ID");
		
		//long data type
		long profit= 4354353558855555554L;
		System.out.println("The profit is  "+ profit);
		
		//float -Accurancy
		//float data type
		float pieValue=3.145545585555555f;
		System.out.println("the pie value is using float is "+ pieValue);
		
		float pieValue2=(float) 3.145545585555555;//Casting to float 
		System.out.println("the pie value is using float is "+ pieValue2);
		
		//double date type
		double pieValue3=3.145545585555555f;
		System.out.println("the pie value is usiing double is "+ pieValue3);
		
		//boolean data type
		boolean status=true;
		System.out.println("The status of the code "+ status);
		
		//Character data type
		char initial ='G';
		System.out.println("The initial is "+ initial );
		
		//String data type
		String name="Sumit";
		System.out.println("Name of the employee is "+ name);
		
		
				
	}

}
